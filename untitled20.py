def build_lenet_1d(input_shape):
    inp = Input(shape=(input_shape, 1))
    x = layers.Conv1D(32, 5, activation="tanh", padding="same")(inp)
    x = layers.MaxPooling1D(2)(x)
    x = layers.Conv1D(8, 5, activation="tanh", padding="same")(x)
    x = layers.MaxPooling1D(2)(x)
    x = layers.Conv1D(16, 1, activation="tanh", padding="same")(x)
    x = layers.Flatten()(x)
    x = layers.Dense(32, activation="tanh")(x)
    x = layers.Dense(16, activation="tanh")(x)
    x = layers.Dense(4, activation="tanh")(x)
    out = layers.Dense(1, activation="sigmoid")(x)
    return Model(inp, out, name="LeNet1D")

def build_gru_small(input_shape):
    inp = Input(shape=(input_shape, 1))
    x = layers.GRU(17, return_sequences=True)(inp)
    x = layers.GRU(17)(x)
    out = layers.Dense(1, activation="sigmoid")(x)
    return Model(inp, out, name="GRU_small")

def build_mlp_meta():
    inp = Input(shape=(2,))
    x = layers.Dense(16, activation="relu")(inp)
    x = layers.Dense(8, activation="relu")(x)
    out = layers.Dense(1, activation="sigmoid")(x)
    return Model(inp, out, name="MLP_meta")

def build_enscvdd_net(input_shape):
    lenet = build_lenet_1d(input_shape)
    gru = build_gru_small(input_shape)
    inp = Input(shape=(input_shape,1))
    out1, out2 = lenet(inp), gru(inp)
    concat = layers.concatenate([out1, out2])
    final = layers.Dense(1, activation="sigmoid")(concat)
    return Model(inp, final, name="EnsCVDD-Net")

