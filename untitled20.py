# ---------------- Hyperparameters ----------------
EPOCHS = 20
BATCH_SIZE = 64
LR = 0.001

# ---------------- Data ----------------
X_all = X_scaled.astype(np.float32)  # Ensure float32
y_all = y_res.astype(np.float32)

skf = StratifiedKFold(n_splits=10, shuffle=True, random_state=SEED)

ens_acc, bl_acc = [], []

for fold, (train_idx, test_idx) in enumerate(skf.split(X_all, y_all), 1):
    print(f"\nFold {fold}")

    # Train/Test Split
    X_train, X_test = X_all[train_idx], X_all[test_idx]
    y_train, y_test = y_all[train_idx], y_all[test_idx]

    # Reshape for 1D CNN/GRU input
    X_train_r = X_train.reshape(-1, n_features, 1)
    X_test_r = X_test.reshape(-1, n_features, 1)

    # ---------------- Create tf.data.Dataset ----------------
    train_ds = tf.data.Dataset.from_tensor_slices((X_train_r, y_train))
    train_ds = train_ds.shuffle(buffer_size=len(X_train_r))
    train_ds = train_ds.batch(BATCH_SIZE)
    train_ds = train_ds.prefetch(tf.data.AUTOTUNE)

    test_ds = tf.data.Dataset.from_tensor_slices((X_test_r, y_test))
    test_ds = test_ds.batch(BATCH_SIZE)
    test_ds = test_ds.prefetch(tf.data.AUTOTUNE)

    # ---------------- EnsCVDD ----------------
    model_ens = build_enscvdd_net(n_features)
    model_ens.compile(optimizer=Adam(LR), loss="binary_crossentropy", metrics=["accuracy"])
    model_ens.fit(train_ds, epochs=EPOCHS, verbose=1)

    preds_ens = (model_ens.predict(test_ds, verbose=0).ravel() >= 0.5).astype(int)
    acc = accuracy_score(y_test, preds_ens)
    ens_acc.append(acc)
    print("EnsCVDD Accuracy:", acc)

    # Save EnsCVDD model
    model_path = f"{project_path}/enscvdd_fold{fold}.h5"
    model_ens.save(model_path)
    print("Saved:", model_path)

    # ---------------- BlCVDD ----------------
    lenet = build_lenet_1d(n_features)
    gru = build_gru_small(n_features)
    lenet.compile(optimizer=Adam(LR), loss="binary_crossentropy")
    gru.compile(optimizer=Adam(LR), loss="binary_crossentropy")

    lenet.fit(train_ds, epochs=EPOCHS, verbose=1)
    gru.fit(train_ds, epochs=EPOCHS, verbose=1)

    # Meta features for MLP
    p1 = lenet.predict(test_ds, verbose=0).ravel()
    p2 = gru.predict(test_ds, verbose=0).ravel()
    meta_features = np.vstack([p1, p2]).T

    mlp = build_mlp_meta()
    mlp.compile(optimizer=Adam(LR), loss="binary_crossentropy")
    mlp.fit(meta_features, y_test, epochs=EPOCHS, batch_size=BATCH_SIZE, verbose=1)

    preds_bl = (mlp.predict(meta_features, verbose=0).ravel() >= 0.5).astype(int)
    acc_b = accuracy_score(y_test, preds_bl)
    bl_acc.append(acc_b)
    print("BlCVDD Accuracy:", acc_b)

